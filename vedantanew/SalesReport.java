    void clientRange()
    {
        java.util.Date from = (Date)fromDate2.getValue();
        java.util.Date to = (Date)toDate2.getValue();
        
        String id = getSelectedItemId();
        if(id != null)
        {
             if(from.compareTo(to) <=0)
            {
                report = new ClientReport();
                report.showReport(id,from,to);
                this.add("report", report);
                ((CardLayout)(this.getLayout())).show(this,"report");

            }
        }
       
    }
    
