 String getSelectedItemId()
 {
     String id =  null;
     if(clientList.getSelectedIndex() !=-1)
        {
                
            String selectedItem = clientList.getSelectedItem().toString();
            id = selectedItem.substring(selectedItem.lastIndexOf(")")+2);
        }
     return id;
 }
void clientToday()
    {
        String id = getSelectedItemId();
        if(id != null)
        {
            report = new ClientReport();
            report.showReport(id,"today");
            this.add("report", report);
            ((CardLayout)(this.getLayout())).show(this,"report");
        }
             
        
   }
